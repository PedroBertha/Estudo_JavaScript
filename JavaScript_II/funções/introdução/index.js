// Funções:

// É uma estrutura do JavaScript que permite declarar um procedimento 
// e executá-lo programaticamente.

// Uma função pode ser vista como:  
// 🔴 — ENTRADA → 🔴 — PROCEDIMENTO → 🔴 — SAÍDA  

// Tanto a entrada quanto a saída de uma função são opcionais, 
// ou seja, uma função pode existir sem um deles ou sem ambos.  

// As entradas da função são chamadas de parâmetros.  
// As saídas da função são chamadas de retorno.  

// Sintaxe:
// function nomeDaFuncao(entradas) {  
    // procedimentos...  
//  return saida;  
// }

